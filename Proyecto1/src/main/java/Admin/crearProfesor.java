    private void CrearBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearBtnActionPerformed
        // TODO add your handling code here:
        
        String codigo = CodigoInp.getText();
        String nom = NombreInp.getText();
        String apellido = ApellidoInp.getText();
        String correo = CorreoInp.getText();
        String contrasena = PassInp.getText();
        String genero = GenreInp.getText();

        Profs nuevoProfesor = new Profs(nom, apellido, codigo, correo, contrasena, genero);
        int contadorProfesores = AdminProfesor.contProfes;

        AdminProfesor.profesores[contadorProfesores] = nuevoProfesor;

        AdminProfesor.contProfes++;
        
        System.out.println("Agregado");
    }//GEN-LAST:event_CrearBtnActionPerformed

