    public void actualizarListaAlumn() {

        if (contAlumnos > 0) {

            // actualizar listado
            DefaultTableModel modeloListado = (DefaultTableModel) ListadoAlumnos.getModel();
            modeloListado.setRowCount(contAlumnos);

            for (int i = 0; i < contAlumnos; i++) {
                modeloListado.setValueAt(alumn[i].codigo, i, 0);
                modeloListado.setValueAt(alumn[i].nombre, i, 1);
                modeloListado.setValueAt(alumn[i].apellido, i, 2);
                modeloListado.setValueAt(alumn[i].acciones, i, 3);
               
            }

            System.out.println(contAlumnos);

        }
    }
    
  
